using System.Collections.Generic;
using System.Linq;
using System.Text;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using Microsoft.CodeAnalysis.Text;

namespace PatternKit.Generators.DistributedLocks;

[Generator]
public sealed class DistributedLockGenerator : IIncrementalGenerator
{
    private const string AttributeName = "PatternKit.Generators.DistributedLocks.GenerateDistributedLockAttribute";

    private static readonly DiagnosticDescriptor MustBePartial = new(
        "PKDLOCK001",
        "Distributed Lock host must be partial",
        "Type '{0}' is marked with [GenerateDistributedLock] but is not declared as partial",
        "PatternKit.Generators.DistributedLocks",
        DiagnosticSeverity.Error,
        true);

    private static readonly DiagnosticDescriptor InvalidConfiguration = new(
        "PKDLOCK002",
        "Distributed Lock configuration is invalid",
        "Distributed Lock '{0}' must have non-empty FactoryMethodName and LockName values and LeaseDurationMilliseconds > 0",
        "PatternKit.Generators.DistributedLocks",
        DiagnosticSeverity.Error,
        true);

    private static readonly SymbolDisplayFormat TypeFormat = new(
        globalNamespaceStyle: SymbolDisplayGlobalNamespaceStyle.Included,
        typeQualificationStyle: SymbolDisplayTypeQualificationStyle.NameAndContainingTypesAndNamespaces,
        genericsOptions: SymbolDisplayGenericsOptions.IncludeTypeParameters,
        miscellaneousOptions: SymbolDisplayMiscellaneousOptions.IncludeNullableReferenceTypeModifier | SymbolDisplayMiscellaneousOptions.UseSpecialTypes);

    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        var candidates = context.SyntaxProvider.ForAttributeWithMetadataName(
            AttributeName,
            static (node, _) => node is TypeDeclarationSyntax,
            static (ctx, _) => (Type: (INamedTypeSymbol)ctx.TargetSymbol, Node: (TypeDeclarationSyntax)ctx.TargetNode, Attributes: ctx.Attributes));

        context.RegisterSourceOutput(candidates, static (spc, candidate) =>
        {
            var attr = candidate.Attributes.FirstOrDefault(static attribute =>
                attribute.AttributeClass?.ToDisplayString() == AttributeName);
            if (attr is not null)
                Generate(spc, candidate.Type, candidate.Node, attr);
        });
    }

    private static void Generate(SourceProductionContext context, INamedTypeSymbol type, TypeDeclarationSyntax node, AttributeData attribute)
    {
        if (!node.Modifiers.Any(static modifier => modifier.Text == "partial"))
        {
            context.ReportDiagnostic(Diagnostic.Create(MustBePartial, node.Identifier.GetLocation(), type.Name));
            return;
        }

        var keyType = attribute.ConstructorArguments.Length >= 1 ? attribute.ConstructorArguments[0].Value as INamedTypeSymbol : null;
        if (keyType is null || keyType.TypeKind == TypeKind.Error)
            return;

        var factoryMethodName = GetNamedString(attribute, "FactoryMethodName") ?? "Create";
        var lockName = GetNamedString(attribute, "LockName") ?? "distributed-lock";
        var leaseMs = GetNamedInt(attribute, "LeaseDurationMilliseconds") ?? 30000;
        if (!IsValidIdentifier(factoryMethodName) || string.IsNullOrWhiteSpace(lockName) || leaseMs <= 0)
        {
            context.ReportDiagnostic(Diagnostic.Create(InvalidConfiguration, node.Identifier.GetLocation(), type.Name));
            return;
        }

        context.AddSource($"{type.Name}.DistributedLock.g.cs", SourceText.From(
            GenerateSource(type, keyType, factoryMethodName, lockName, leaseMs),
            Encoding.UTF8));
    }

    private static string GenerateSource(INamedTypeSymbol type, INamedTypeSymbol keyType, string factoryMethodName, string lockName, int leaseDurationMilliseconds)
    {
        var ns = type.ContainingNamespace.IsGlobalNamespace ? null : type.ContainingNamespace.ToDisplayString();
        var keyTypeName = keyType.ToDisplayString(TypeFormat);
        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        if (ns is not null)
        {
            sb.Append("namespace ").Append(ns).AppendLine(";");
            sb.AppendLine();
        }

        var indent = string.Empty;
        foreach (var containingType in GetContainingTypes(type))
        {
            AppendTypeDeclaration(sb, containingType, indent);
            sb.Append(indent).AppendLine("{");
            indent += "    ";
        }

        AppendTypeDeclaration(sb, type, indent);
        sb.Append(indent).AppendLine("{");
        var memberIndent = indent + "    ";
        var bodyIndent = memberIndent + "    ";
        sb.Append(memberIndent).Append("public static global::PatternKit.Cloud.DistributedLocks.DistributedLock<").Append(keyTypeName).Append("> ").Append(factoryMethodName).AppendLine("()");
        sb.Append(memberIndent).AppendLine("{");
        sb.Append(bodyIndent).Append("return global::PatternKit.Cloud.DistributedLocks.DistributedLock<").Append(keyTypeName).Append(">.Create(\"").Append(Escape(lockName)).AppendLine("\")");
        sb.Append(bodyIndent).Append("    .LeaseDuration(global::System.TimeSpan.FromMilliseconds(").Append(leaseDurationMilliseconds).AppendLine("))");
        sb.Append(bodyIndent).AppendLine("    .Build();");
        sb.Append(memberIndent).AppendLine("}");
        sb.Append(indent).AppendLine("}");
        while (indent.Length > 0)
        {
            indent = indent.Substring(0, indent.Length - 4);
            sb.Append(indent).AppendLine("}");
        }

        return sb.ToString();
    }

    private static IReadOnlyList<INamedTypeSymbol> GetContainingTypes(INamedTypeSymbol type)
    {
        var stack = new Stack<INamedTypeSymbol>();
        for (var current = type.ContainingType; current is not null; current = current.ContainingType)
            stack.Push(current);
        return stack.ToArray();
    }

    private static void AppendTypeDeclaration(StringBuilder sb, INamedTypeSymbol type, string indent)
    {
        sb.Append(indent).Append(GetAccessibility(type.DeclaredAccessibility)).Append(' ');
        if (type.IsStatic)
            sb.Append("static ");
        else if (type.IsAbstract && type.TypeKind == TypeKind.Class)
            sb.Append("abstract ");
        else if (type.IsSealed && type.TypeKind == TypeKind.Class)
            sb.Append("sealed ");
        sb.Append("partial ");
        if (type.IsRecord)
            sb.Append(type.TypeKind == TypeKind.Struct ? "record struct" : "record class");
        else
            sb.Append(type.TypeKind == TypeKind.Struct ? "struct" : "class");
        sb.Append(' ').Append(type.Name).AppendLine();
    }

    private static string? GetNamedString(AttributeData attribute, string name)
        => attribute.NamedArguments.FirstOrDefault(kv => kv.Key == name).Value.Value as string;

    private static int? GetNamedInt(AttributeData attribute, string name)
        => attribute.NamedArguments.FirstOrDefault(kv => kv.Key == name).Value.Value as int?;

    private static string Escape(string value) => value.Replace("\\", "\\\\").Replace("\"", "\\\"");

    private static bool IsValidIdentifier(string? value)
        => !string.IsNullOrWhiteSpace(value)
           && SyntaxFacts.IsValidIdentifier(value)
           && SyntaxFacts.GetKeywordKind(value) == SyntaxKind.None
           && SyntaxFacts.GetContextualKeywordKind(value) == SyntaxKind.None;

    private static string GetAccessibility(Accessibility accessibility)
        => accessibility switch
        {
            Accessibility.Public => "public",
            Accessibility.Internal => "internal",
            Accessibility.Private => "private",
            Accessibility.Protected => "protected",
            Accessibility.ProtectedAndInternal => "private protected",
            Accessibility.ProtectedOrInternal => "protected internal",
            _ => "internal"
        };
}
